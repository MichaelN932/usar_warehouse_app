// Helper: Get items by category
export function getKitItemsByCategory(category: string): KitItem[] {
  return NEW_MEMBER_KIT.filter(item => item.category === category);
}

// Helper: Get all unique categories in order
export function getKitCategories(): string[] {
  const categories: string[] = [];
  NEW_MEMBER_KIT.forEach(item => {
    if (!categories.includes(item.category)) {
      categories.push(item.category);
    }
  });
  return categories;
}

// Helper: Get only issued items (from cache)
export function getIssuedItems(): KitItem[] {
  return NEW_MEMBER_KIT.filter(item => item.isIssued);
}

// Helper: Get only personal items
export function getPersonalItems(): KitItem[] {
  return NEW_MEMBER_KIT.filter(item => !item.isIssued);
}

// Helper: Count totals
export function getKitTotals() {
  const issued = NEW_MEMBER_KIT.filter(item => item.isIssued);
  const personal = NEW_MEMBER_KIT.filter(item => !item.isIssued);

  return {
    totalItems: NEW_MEMBER_KIT.length,
    issuedItems: issued.length,
    personalItems: personal.length,
    totalQuantity: NEW_MEMBER_KIT.reduce((sum, item) => sum + item.qtyRequired, 0),
    issuedQuantity: issued.reduce((sum, item) => sum + item.qtyRequired, 0),
    personalQuantity: personal.reduce((sum, item) => sum + item.qtyRequired, 0),
  };
}
