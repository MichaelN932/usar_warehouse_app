// New Member Kit Template
// Based on CA-TF2 Equipment Issue Form
// Contains all items required for new team member gear distribution

export type SizeCategory = 'oneSize' | 'clothing' | 'footwear' | 'gloves' | 'truSpec' | 'fiveElevenPants';

export type BagLocation =
  | 'Deployment Uniform'
  | '24 HR Bag'
  | 'Roller Bag'
  | 'Web Gear'
  | 'Web Gear Bag'
  | 'Cold Weather Bag';

export interface BrandOption {
  brand: string;              // e.g., 'Tru-Spec', '5.11'
  sizeCategory: SizeCategory; // Which size array to use
  isDefault?: boolean;        // Pre-selected brand
}

export interface SizingConfig {
  type: 'oneSize' | 'sized';
  brandOptions?: BrandOption[];       // If multiple brands available
  defaultSizeCategory?: SizeCategory; // Fallback if no brand options
  profileField?: 'shirt' | 'pants' | 'bootSize' | 'gloveSize'; // Which user profile field to auto-fill from
}

export interface KitItem {
  id: string;                    // Unique identifier for the item
  category: string;              // Category grouping
  itemName: string;              // Display name
  sizing: SizingConfig;          // NEW: Sizing configuration with brand support
  qtyRequired: number;           // Standard quantity for new member
  isIssued: boolean;             // true = from cache (Issue), false = personal item
  notes?: string;                // Special instructions or notes
  bagLocations?: BagLocation[];  // Where item goes (can be multiple bags)
}

